// Empaqueta bpmn-design/** + bpmn-design-app/** en un módulo ESM embebido.
// Fuente única: editá los .md/.bpmn/.py fuente y re-corré `npm run pack:bpmn-design`.
import { readFileSync, writeFileSync, readdirSync, statSync } from "node:fs";
import { createHash } from "node:crypto";
import { join, relative } from "node:path";

const ROOT = process.cwd();
const OUT = "src/processDocs/bpmnDesignSkill.generated.ts";

// (dirFuente, prefijoDestino) → keys destino relativas a _bpmn-design/
const SOURCES = [
  ["bpmn-design", ""],
  ["bpmn-design-app", "app/"],
];

function walk(dir) {
  const out = [];
  for (const name of readdirSync(dir)) {
    const p = join(dir, name);
    if (statSync(p).isDirectory()) out.push(...walk(p));
    else out.push(p);
  }
  return out;
}

const files = {};
for (const [srcDir, prefix] of SOURCES) {
  for (const abs of walk(join(ROOT, srcDir))) {
    const rel = relative(join(ROOT, srcDir), abs).split("\\").join("/");
    files[prefix + rel] = readFileSync(abs, "utf8");
  }
}

const keys = Object.keys(files).sort();
const hash = createHash("sha256");
for (const k of keys) hash.update(k + "\0" + files[k] + "\0");
const version = hash.digest("hex").slice(0, 12);

const body =
  `// GENERATED by scripts/pack-bpmn-design.mjs — DO NOT EDIT.\n` +
  `// Fuente: bpmn-design/** + bpmn-design-app/**. Re-generá con: npm run pack:bpmn-design\n` +
  `export const BPMN_DESIGN_VERSION = ${JSON.stringify(version)};\n\n` +
  `export const BPMN_DESIGN_FILES: Record<string, string> = {\n` +
  keys.map((k) => `  ${JSON.stringify(k)}: ${JSON.stringify(files[k])},`).join("\n") +
  `\n};\n`;

writeFileSync(join(ROOT, OUT), body);
console.log(`packed ${keys.length} files, version ${version} -> ${OUT}`);
